package com.idacchi.cloudautomator;

/**
 * Cloud Automator Setting
 * 
 * @author yasuhiko
 *
 */
public class SettingCA {

	/** URL */
	private String url;

	/** CAAuth token */
	private String auth;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getAuth() {
		return auth;
	}

	public void setAuth(String auth) {
		this.auth = auth;
	}

}
